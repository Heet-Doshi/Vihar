     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $addtran=new addtran();
        $addtran->name=$request->input('name');
        $addtran->email=$request->input('email');
        $addtran->address=$request->input('address');
        $addtran->amount=$request->input('amount');
        $addtran->area=$request->input('area');
        $addtran->transaction_id=$request->input('tran_id');
        $addtran->total=$request->input('total');
        $addtran->save();
        return redirect("home");
    }

