     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $guide =new guide();
        $guide->name=$request->input('name');
        $guide->address=$request->input('address');
        $guide->true=$request->input('true');
        $guide->mobile=$request->input('mobile');
        $guide->latitude=$request->input('latitude');
        $guide->longitude=$request->input('longitude');
        $guide->save();
        return redirect("guidehome");
    }

